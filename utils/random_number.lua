function pseudo_random( index, min, max )
    min = min or 0
    max = max or 1

    if index < 1 or index > #pseudo_random_number then index = 1 end

    local ret_number = pseudo_random_number[index]
    ret_number = ret_number * ( max - min ) + min

    return ret_number, index + 1
end

